public class Operators {
    public static void main(String[] args){
        System.out.println(3+3);
        System.out.println('a'); //ascii value of 'a'
        System.out.println('a'+3);
        System.out.println((char)('a'+3)); //d
        System.out.println("'a' + 'b' "+ ('a' + 'b')); //ascii value of a and b
        System.out.println("\"a\" + \"b\" " + ("a" + "b")); // prints: "a" + "b" ab
        System.out.println("\"a\" +1 = " + ("a" +1)); //1 converted to string and concatenated with "a"

        
    }
}
